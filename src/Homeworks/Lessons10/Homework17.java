package Homeworks.Lessons10;

public class Homework17 {


    public static void main(String[] args) {

        int j = 10;
        do {
            System.out.println(j);
            j--;
        }
        while (j >= 3);

        System.out.println("\n" +
                "Congratulations! You found your lucky number '3'");

    }
}
